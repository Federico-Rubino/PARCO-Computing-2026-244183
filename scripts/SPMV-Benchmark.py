import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import os
import argparse

# --- Argument parser ---
parser = argparse.ArgumentParser(description="SPMV log analyzer")
parser.add_argument(
    "lod_dir",
    type=str,
    help="Percorso al file CSV di log"
)
args = parser.parse_args()

logname = args.lod_dir
header_vars = {}

with open(logname, "r") as f:
    h1 = f.readline().strip()  # es. "#matrice1.mtx 1000 20000 32"
    h2 = f.readline().strip()  # es. "#1 16 1"
    h3 = f.readline().strip()  # es. "#1000 20000 100"

# Funzione di utilità: rimuove il "#" iniziale e splitta
def parse_line(line):
    line = line.lstrip("#").strip()
    return line.split()

# --- Linea 1: filename, row, col, nnz ---
p1 = parse_line(h1)
filename = p1[0]
row, col, nnz = map(int, p1[1:])

# --- Linea 2: n_thread_start, n_thread_end, step ---
n_thread_start, n_thread_end, thread_step = map(int, parse_line(h2))

# --- Linea 3: chunk_size_start, chunk_size_end, step ---
chunk_size_start, chunk_size_end, chunk_step = map(int, parse_line(h3))

# (opzionale) raccolta in dizionario
header_vars = {
    "filename": filename,
    "row": row,
    "col": col,
    "nnz": nnz,
    "n_thread_start": n_thread_start,
    "n_thread_end": n_thread_end,
    "thread_step": thread_step,
    "chunk_size_start": chunk_size_start,
    "chunk_size_end": chunk_size_end,
    "chunk_step": chunk_step
}

os.makedirs(header_vars['filename'], exist_ok=True)
print(header_vars)

df = pd.read_csv(logname, comment="#")

# Normalize chunks
df['chunks'] = df['chunks'].replace("", np.nan)

# Assign 0 to serial chunks
df.loc[df['schedule_type'] == 'serial', 'chunks'] = 0

df['chunks'] = df['chunks'].astype(int)

# Compute 90P for each type_sched for each chunks for each thread
p90 = (
    df.groupby(['n_thread', 'schedule_type', 'chunks'])['elapsed_time']
      .quantile(0.90)
      .reset_index(name='p90')
)

# Select the best chunks
best = (
    p90.sort_values('p90')
       .groupby(['n_thread', 'schedule_type'])
       .first()
       .reset_index()
)


# p90 for serial (only one thread)
serial_p90_value = best.loc[best['schedule_type'] == 'serial', 'p90'].iloc[0]

# propagate serial timing to all thread
all_threads = sorted(best['n_thread'].unique())
serial_expanded = pd.DataFrame({
    'n_thread': all_threads,
    'schedule_type': 'serial',
    'p90': serial_p90_value,
    'chunks': 0
})

# insert the propagated serial in the dataframe
best = best[best['schedule_type'] != 'serial']
best = pd.concat([best, serial_expanded], ignore_index=True)

# Timing comparison plot
plt.figure(figsize=(10,6))

for sched in best['schedule_type'].unique():
    subset = best[best['schedule_type'] == sched]
    plt.plot(
        subset['n_thread'],
        subset['p90'],
        marker='o',
        label=sched
    )

plt.xlabel("n_thread " + str(header_vars['n_thread_start']) + " to " + str(header_vars['n_thread_end']))
plt.ylabel("elapsed time (90th percentile)")
plt.title(str(header_vars['filename'])+" SPMV – 90P vs thread count (best chunk per scheduling)\n nnz = "+str(header_vars['nnz']))
plt.grid(True)
plt.legend()
plt.tight_layout()


out_path = os.path.join(header_vars['filename'], f"{header_vars['filename']}_90P_vs_threads.png")
plt.savefig(out_path, dpi=300, bbox_inches="tight")
plt.show()

#plot best chunck respect to thread for static scheduling
static_bestchunk = (best[best['schedule_type'] == 'static'])
plt.plot(
    static_bestchunk['n_thread'],
    static_bestchunk['chunks'],
    marker='o',
    label='static'
)
plt.xlabel("n_thread " + str(header_vars['n_thread_start']) + " to " + str(header_vars['n_thread_end']))
plt.ylabel("Chunks dimension " + str(header_vars['chunk_size_start']) + " to " + str(header_vars['chunk_size_end']))
plt.title(str(header_vars['filename'])+" – Best chunk size for static scheduling\n nnz = "+str(header_vars['nnz']))

out_path = os.path.join(header_vars['filename'], f"{header_vars['filename']}_static_chunks.png")
plt.savefig(out_path, dpi=300, bbox_inches="tight")
plt.show()

#plot best chunck respect to thread for dynamic scheduling
dynamic_bestchunk = (best[best['schedule_type'] == 'dynamic'])
plt.plot(
    dynamic_bestchunk['n_thread'],
    dynamic_bestchunk['chunks'],
    marker='o',
    label='dynamic'
)
plt.xlabel("n_thread " + str(header_vars['n_thread_start']) + " to " + str(header_vars['n_thread_end']))
plt.ylabel("Chunks dimension " + str(header_vars['chunk_size_start']) + " to " + str(header_vars['chunk_size_end']))
plt.title(str(header_vars['filename'])+" – Best chunk size for dynamic scheduling\n nnz = "+str(header_vars['nnz']))

out_path = os.path.join(header_vars['filename'], f"{header_vars['filename']}_dynamic_chunks.png")
plt.savefig(out_path, dpi=300, bbox_inches="tight")
plt.show()

#plot best chunck respect to thread for guided scheduling
guided_bestchunk = (best[best['schedule_type'] == 'guided'])
plt.plot(
    guided_bestchunk['n_thread'],
    guided_bestchunk['chunks'],
    marker='o',
    label='guided'
)
plt.xlabel("n_thread " + str(header_vars['n_thread_start']) + " to " + str(header_vars['n_thread_end']))
plt.ylabel("Chunks dimension " + str(header_vars['chunk_size_start']) + " to " + str(header_vars['chunk_size_end']))
plt.title(str(header_vars['filename'])+" – Best chunk size for guided scheduling\n nnz = "+str(header_vars['nnz']))

out_path = os.path.join(header_vars['filename'], f"{header_vars['filename']}_guided_chunks.png")
plt.savefig(out_path, dpi=300, bbox_inches="tight")
plt.show()
